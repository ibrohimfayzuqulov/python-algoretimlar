"""Algaretimga misollar"""
# [ ossonroq ]
# foydalanuvchi kiritgan ikki sonni qo'shish algaretimi
# 1.start
# 2.son1,son2,va summa o'zgaruvchilarni yaratamiz 
# 3.son1 va son2 qiymatlarini qabul qilamiz
# 4.son1 va son2 qo'shamiz,natijani summa ga yuklaymiz:
# 5.summani foydalanuvchiga qaytaramiz
# 6.stop

# [qiyinroq]
# 1. start
# 2. a,b va c o'zgaruvchilarni yaratamiz
# 3. a,b va c ha qiymat beramiz
# 
# 4  agar a>b bo'lsa
#       agar a>c bo'lsa
#             a eng katta son deb qaytar      

# 5 aks holda(ya'ni a<b) bo'lsa
#       agar b>c bo'lsa
#          b eng katta son deb qaytar
#       aks holda
# c eng katta son deb qaytar
# -----------------------------------------------------------------------------------------------------------------------

# N faktorialni hisoblash algoritimi
# 1 start
# N faktorial deb 1 dan N gacha bo'lgan sonlarni ko'paytmasiga aytiladi masalan: (N=5 bo'lsa,5 faktorial 1*2*3*4*5=120 ga teng)
# 2 N,faktorial va i o'zgaruvchilarni yaratamiz 
# 3 faktorial va i ga 1 qiymatni yuklaymiz: (factorial=1, i=1)
# 4 foydalanuvchidan N qiymatini qabul qilamiz
# 5 i=N bo'lgunga qadar quydagi qadamlarni takrorlaymiz         1.faktorial = faktorial * i
#                                                               2.i = i + 1 
# 6 faktorial qiymatni qaytar